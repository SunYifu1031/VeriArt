def build_works_from_artists(artist_entries: List[Tuple]) -> List[Dict]:
    """为所有艺术家构建作品节点"""
    seen_works = set()
    work_nodes = []
    edges = []
    work_idx = 1

    for entry in artist_entries:
        (name, birth, death, style, school, province, works_list, artist_url) = entry
        artist_id = f"artist_{name}"

        for wname in works_list:
            dedup_key = f"{wname}_{name}"
            if dedup_key in seen_works:
                continue
            seen_works.add(dedup_key)

            # 估算创作年份
            if birth:
                mid = birth + 35
                if death:
                    mid = min(mid, death - 3)
                creation_date = str(mid)
            else:
                creation_date = "当代"

            wid = f"work_{work_idx:05d}"
            work_idx += 1

            medium = ("布面油画" if "油画" in style else
                      "绢本设色" if "工笔" in style else
                      "纸本水墨")

            node = {
                "id": wid,
                "label": wname,
                "type": "Work",
                "properties": {
                    "creation_date": creation_date,
                    "material":      medium,
                    "style":         style,
                    "artist_name":   name,
                    "source":        "manual_verified",
                    "source_url":    artist_url,
                    "source_id":     wid,
                }
            }
            work_nodes.append(node)
            edges.append({"source": wid, "target": artist_id, "relation": "CREATED_BY"})

    # 追加 EXTRA_WORKS
    for w in EXTRA_WORKS:
        artist_id = f"artist_{w['artist']}"
        wid = f"work_{work_idx:05d}"
        work_idx += 1
        dedup_key = f"{w['title']}_{w['artist']}"
        if dedup_key in seen_works:
            continue
        seen_works.add(dedup_key)

        node = {
            "id": wid,
            "label": w["title"],
            "type": "Work",
            "properties": {
                "creation_date": w["date"],
                "material":      w["medium"],
                "dimensions":    w.get("dims", ""),
                "museum":        w.get("museum", ""),
                "source":        "manual_verified",
                "source_url":    baidu_url(w["title"]),
                "source_id":     wid,
            }
        }
        work_nodes.append(node)
        edges.append({"source": wid, "target": artist_id, "relation": "CREATED_BY"})

    return work_nodes, edges


def verify_urls_sample(artist_entries, sample_size=20):
    """抽样验证URL可访问性"""
    import urllib.request
    import random

    print(f"\n验证 URL 可访问性（抽样 {sample_size} 个）...")
    samples = random.sample(artist_entries, min(sample_size, len(artist_entries)))
    ok, fail = 0, 0
    failed_urls = []

    for entry in samples:
        url = entry[7]
        try:
            req = urllib.request.Request(url,
                headers={"User-Agent": "Mozilla/5.0 (compatible; VeriArtBot/1.0)"})
            with urllib.request.urlopen(req, timeout=8) as resp:
                if resp.status == 200:
                    ok += 1
                else:
                    fail += 1
                    failed_urls.append(url)
        except Exception:
            fail += 1
            failed_urls.append(url)

    print(f"  可访问: {ok}, 失败: {fail}")
    if failed_urls:
        print("  失败的 URL:")
        for u in failed_urls:
            print(f"    {u}")
    return ok, fail, failed_urls


