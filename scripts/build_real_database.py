def build_unique_artists(raw: List[Tuple]) -> List[Dict]:
    """去重并构建艺术家节点"""
    seen = set()
    artists = []
    for entry in raw:
        name = entry[0]
        if name in seen:
            continue
        seen.add(name)
        (name, birth, death, style, school, province, works, url) = entry
        node = {
            "id": f"artist_{name}",
            "label": name,
            "type": "Artist",
            "properties": {
                "birth_year":  str(birth) if birth else "",
                "death_year":  str(death) if death else "",
                "style":       style,
                "school":      school,
                "province":    province,
                "source":      "manual_verified",
                "source_url":  url,
                "source_id":   f"artist_{name}",
            }
        }
        artists.append((node, works, url))
    return artists


